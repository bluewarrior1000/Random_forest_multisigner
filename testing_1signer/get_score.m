%function to return the score of forests
function score = get_score(eval_opts)
    if isempty(eval_opts.numtrees)
        load(sprintf('%s%s/video%d/pred_joints_width_%d_depth_%d.mat',...
            eval_opts.results_dir, eval_opts.video_type, ...
            eval_opts.video_num,eval_opts.windowwidth,...
            eval_opts.treedepth));
        score = accuracy(eval_opts.thresh_dist+1,:);
    else
        load(sprintf('%s%s/video%d/pred_joints_width_%d_depth_%d_numtrees_%d.mat',...
            eval_opts.results_dir, eval_opts.video_type, ...
            eval_opts.video_num,eval_opts.windowwidth,...
            eval_opts.treedepth,eval_opts.numtrees));
        score = accuracy(eval_opts.thresh_dist+1,:);
    end
end